      <p>The roots of the Conewago Canoe Club can be traced to a letter 
    that George and Carol Figdore wrote to Canoe magazine in 1973.&nbsp; 
    Weary of setting up their own shuttles and canoeing alone, they wanted 
    to find other canoers in the York area. &nbsp;Cleathon Myers' response 
    to that letter set in motion a series of events which generated a list 
    of non-members for a non-organized canoe club.&nbsp; Not until late in 
    1974 did the group become officially organized as the Conewago Canoe 
    Club.</p>
    <p>Basically a flatwater group in its infancy, the paddlers named their 
    new organization after a stream on which many early trips were 
    taken.&nbsp; Ironically, Conewago is an Indian word meaning “big water.”</p>
    <p>The Club now offers canoe and kayak courses for the beginner, the 
    intermediate, and the advanced paddler.&nbsp; In four months under club 
    guidance a new paddler can accumulate enough skills to successfully 
    negotiate Class III to IV water.&nbsp;Club members have logged thousands
     of miles across the continent making friends and spreading the name 
    Conewago.&nbsp; Over the years some members have been motivated to 
    become politically involved on the local, state, and national levels in 
    such issues as river conservation, land preservation, non-powered boat 
    registration, and the fight against pollution.</p>
